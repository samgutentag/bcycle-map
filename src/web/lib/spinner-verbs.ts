export function getRandomVerb(set: 'claude' | 'bike' = 'bike'): string {
  const verbs = set === 'claude' ? CLAUDE_VERBS : BIKE_VERBS
  return verbs[Math.floor(Math.random() * verbs.length)]!
}

export function useStableVerb(set: 'claude' | 'bike' = 'bike'): string {
  // eslint-disable-next-line react-hooks/exhaustive-deps
  return useMemo(() => getRandomVerb(set), [set])
}
